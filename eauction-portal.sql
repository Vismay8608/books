-- 2. BRANCH LEVELS (per company — each company defines its own hierarchy levels)
-- Step 1 of registration: company defines levels before creating branches.
-- Example: Company A → HO(1) > ZO(2) > RO(3) > BO(4)
--          Company B → HQ(1) > Circle(2) > District(3) > Local(4)
    CREATE TABLE client_branch_levels (
        level_id     INTEGER      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        clientid     INTEGER      NOT NULL REFERENCES client_register(clientid) ON DELETE CASCADE,
        level_code   VARCHAR(20)  NOT NULL,
        label        VARCHAR(100) NOT NULL,
        level_order  SMALLINT     NOT NULL CHECK (level_order > 0),  -- 1 = top of hierarchy
        is_admin_branch BOOLEAN      NOT NULL DEFAULT FALSE, -- TRUE = root branch level set at client onboarding by platform admin; client cannot change (enforced by trigger)
        is_active    BOOLEAN      NOT NULL DEFAULT TRUE,
        created_on   TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP,
        created_by   INTEGER,
        CONSTRAINT uq_client_level_code  UNIQUE (clientid, level_code),
        CONSTRAINT uq_client_level_order UNIQUE (clientid, level_order)
    );
    CREATE INDEX idx_cbl_client ON client_branch_levels(clientid, level_order);
    CREATE UNIQUE INDEX uq_cbl_is_admin_branch ON client_branch_levels(clientid) WHERE is_admin_branch = TRUE;

    -- 3. BRANCHES
    CREATE TABLE client_branches (
        branchid       INTEGER      GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
        clientid       INTEGER      NOT NULL REFERENCES client_register(clientid),
        level_id       INTEGER      NOT NULL REFERENCES client_branch_levels(level_id),
        parent_id      INTEGER      REFERENCES client_branches(branchid),   -- direct parent; NULL = root
        branch_code    VARCHAR(50)  NOT NULL UNIQUE,
        branch_name    VARCHAR(255) NOT NULL,
        branch_country VARCHAR(100),
        branch_state   VARCHAR(100),
        branch_district VARCHAR(100),
        branch_city    VARCHAR(100),
        branch_address TEXT,
        postal_code    VARCHAR(20),
        phone_primary  VARCHAR(20),
        phone_secondary VARCHAR(20),
        email_primary  VARCHAR(255),
        email_secondary VARCHAR(255),
        status         VARCHAR(30)  DEFAULT 'ACTIVE' REFERENCES master_status(code),
        -- System-generated admin branch: cannot be deleted or have flag cleared
        is_admin_branch BOOLEAN     NOT NULL DEFAULT FALSE,
        can_be_deleted  BOOLEAN     NOT NULL DEFAULT TRUE,
        created_by     INTEGER,
        created_at     TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP,
        updated_by     INTEGER,
        updated_at     TIMESTAMPTZ
    );


